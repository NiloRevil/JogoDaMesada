    private void iniciaServer(){
        if(controller != null){//se o controller existir
			jButton1.setEnabled(false);//desabilita o bot達o de inciar
			int i = 1099;//porta padr達o
			String portaS = jTextFieldPorta.getText();//recupera a porta do usuario
			try{
				i = Integer.parseInt(portaS);//converte a string em int
			}catch(NumberFormatException e){
				jTextArea2.setText("Erro ao digitar porta, porta escolhida padrao: 1099\n");//caso n達o seja valida a porta avisa ao usuario e usa a porta padr達o
			}
			controller.iniciaServer(i, jTextArea2);//inicia o servidor
		}
    }
    

